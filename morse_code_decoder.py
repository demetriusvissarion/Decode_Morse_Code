def decode_morse(morse_code):
    MORSE_CODE["space"]= " "
    decode_list = {v: k for v, k in MORSE_CODE.items()}
    striped_morse_code = morse_code.strip()
    spaces = striped_morse_code.replace("   ", " space ").split(" ")
    return "".join(decode_list[x] for x in spaces)
    
    
